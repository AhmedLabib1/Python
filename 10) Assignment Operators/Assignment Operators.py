# --------------------------
# -- Assignment Operators --
# --------------------------
# [1] =   (Assignment Operator)
# [2] +=  (Add and Assign)
# [3] -=  (Subtract and Assign)
# [4] *=  (Multiply and Assign)
# [5] /=  (Divide and Assign)
# [6] %=  (Modulus and Assign)
# [7] //= (Floor Division and Assign)
# [8] **= (Exponentiation and Assign)
# --------------------------
x = 5 # Assignment Operator

x += 5 # Add and Assign
print(x) # Ouput: 10

x -= 5 # Subtract and Assign
print(x) # Ouput: 5

x *= 5 # Multiply and Assign
print(x) # Output: 25

x /= 5 # Divide and Assign
print(x) # Output: 5.0

x %= 3 # Modulus and Assign
print(x) # Output: 2.0

x //= 0.3 # Floor Division and Assign
print(x) # Output: 6.0

x **= 2 # Multiply and Assign
print(x) # Output: 36.0